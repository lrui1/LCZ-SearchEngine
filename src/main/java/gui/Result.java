    private void url5ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        List<ResultEntry> results;
        Search search = new EsSearch();
        results = search.search(Input.getText(),(Page.page+1)/2);
        Desktop desktop = Desktop.getDesktop();
        URI uri;
        if((Page.page%2)==1){
            try {
                uri = new URI(results.get(4).getUrl());
            } catch (URISyntaxException e) {
                throw new RuntimeException(e);
            }
        }else{
            try {
                uri = new URI(results.get(9).getUrl());
            } catch (URISyntaxException e) {
                throw new RuntimeException(e);
            }
        }


        try {
            desktop.browse(uri);//使用默认浏览器打开超链接
        } catch (IOException e) {
            throw new RuntimeException(e);
        }
        search.close();


    }

